<?php
// Define two numbers
$number1 = 10;
$number2 = 3;

// Addition
$sum = $number1 + $number2;
echo "The sum of $number1 and $number2 is: $sum\n";

// Subtraction
$difference = $number1 - $number2;
echo "The difference between $number1 and $number2 is: $difference\n";

// Multiplication
$product = $number1 * $number2;
echo "The product of $number1 and $number2 is: $product\n";

// Division
$quotient = $number1 / $number2;
echo "The quotient of $number1 divided by $number2 is: $quotient\n";

// Modulus
$remainder = $number1 % $number2;
echo "The remainder of $number1 divided by $number2 is: $remainder\n";
?>
